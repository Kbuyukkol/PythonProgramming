grade_level = int(input("What is your grade level number\n"))

if 1 <= grade_level <= 5:
    print("Elementary school ")
if 6 <= grade_level <= 8:
    print("Middle school ")
if 9 <= grade_level <= 12:
    print("High school ")
if 13 <= grade_level <= 16:
    print("College")
if 17 <= grade_level <= 18:
    print("Grad School")
